# Basic program in Python - Sequential Code
x=5
print(x)
x=x+3
print(x)
print ("=> End Sequential Code")

# Basic program in Python - Conditional Code
x=15
print("x:",x)
if x<10:
    print("Smaller")
else:
    print("Bigger 10")
if x>20:
    print("Bigger 20")
else:
    print("Smaller 20")
print ("=> End Conditional Code")

# Basic program in Python - Repeated Code
while x>0:
    print(x)
    x=x-1
print ("=> End Loop or Repeated Code")
